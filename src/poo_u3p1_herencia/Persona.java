
package poo_u3p1_herencia;

public class Persona {
    String rfc;
    String nombre;
    
    public void setRfc(String rfc){
        this.rfc = rfc;
    }
    public void setNombre(String nombre){
        this.nombre = nombre;
    }
    public String getRfc(){
        return this.rfc;
    }
    public String getNombre(){
        return this.nombre;
    }
   
}
